# The formula for binomial distribution is P(X=x) = nCx * p^x * (1-p)^(n-x), where: 
# P(X=x): is the probability of exactly x successes.
# nCx: is the binomial coefficient, representing the number of combinations to choose x successes from n trials.
# p: is the probability of success on a single trial.
# x: is the number of successful outcomes.
# n: is the total number of independent trials.
# (1-p): is the probability of failure on a single trial, often denoted as q.
